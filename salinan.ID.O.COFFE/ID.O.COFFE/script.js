/* ==========================================================================
   PARALLAX HERO – mouse & touch move
   ========================================================================== */
function applyParallax(xRatio, yRatio) {
    // Character: moves slightly opposite to cursor for depth feel
    const maxMove = 20;
    const imgX = (xRatio - 0.5) * maxMove;
    const imgY = (yRatio - 0.5) * maxMove;
    heroImg.style.transform = `translate(${imgX}px, ${imgY}px)`;

    // Deco elements: move a bit more (faster layer)
    const d = 35;
    decoBean.style.transform = `translate(${(xRatio - 0.5) * d * -1}px, ${(yRatio - 0.5) * d * -1}px)`;
    decoStar.style.transform = `translate(${(xRatio - 0.5) * d * 1.5}px, ${(yRatio - 0.5) * d * 1.5}px)`;
    decoCup.style.transform  = `translate(${(xRatio - 0.5) * d * -2}px, ${(yRatio - 0.5) * d * -0.8}px)`;
}

heroSection.addEventListener('mousemove', (e) => {
    const rect = heroSection.getBoundingClientRect();
    const xRatio = (e.clientX - rect.left) / rect.width;
    const yRatio = (e.clientY - rect.top) / rect.height;
    applyParallax(xRatio, yRatio);
});

heroSection.addEventListener('mouseleave', () => {
    heroImg.style.transform = 'translate(0, 0)';
    decoBean.style.transform = '';
    decoStar.style.transform = '';
    decoCup.style.transform  = '';
});

// Touch support for mobile
heroSection.addEventListener('touchmove', (e) => {
    const touch = e.touches[0];
    const rect = heroSection.getBoundingClientRect();
    const xRatio = (touch.clientX - rect.left) / rect.width;
    const yRatio = (touch.clientY - rect.top) / rect.height;
    applyParallax(xRatio, yRatio);
}, { passive: true });

