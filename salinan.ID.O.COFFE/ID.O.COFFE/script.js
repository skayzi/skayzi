function renderMenu(categoryFilter) {
    menuContainer.innerHTML = '';
    const filtered = categoryFilter === 'all'
        ? menuItems
        : menuItems.filter(item => item.category === categoryFilter);

    const addLabel = translations[currentLang].btn_add;

    filtered.forEach(item => {
        const card = document.createElement('div');
        card.className = 'menu-card';
        card.innerHTML = `
            <img src="${item.img}" alt="${item.name}" class="menu-card-img">
            <h3>${item.name} ${item.icon}</h3>
            <div class="menu-price">Rp ${item.price.toLocaleString('id-ID')}</div>
            <button class="btn btn-primary add-to-cart" onclick="addToCart(${item.id})">${addLabel}</button>
        `;
        menuContainer.appendChild(card);
    });
}

categoryBtns.forEach(btn => {
    btn.addEventListener('click', () => {
        categoryBtns.forEach(b => b.classList.remove('active'));
        btn.classList.add('active');
        renderMenu(btn.getAttribute('data-filter'));
    });
});

