/* ==========================================================================
   i18n – BILINGUAL SYSTEM
   ========================================================================== */
function applyLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('lang', lang);

    // Update all data-i18n elements
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang][key] !== undefined) {
            el.innerHTML = translations[lang][key];
        }
    });

    // Update lang button label
    langToggle.textContent = lang === 'id' ? '🇺🇸 EN' : '🇮🇩 ID';

    // Re-render menu with updated "add" button text
    const activeFilter = document.querySelector('.category-btn.active')?.dataset.filter || 'all';
    renderMenu(activeFilter);

    // Update empty cart message if shown
    const emptyMsg = document.querySelector('.empty-cart-msg');
    if (emptyMsg) emptyMsg.innerHTML = translations[lang].cart_empty;
}

langToggle.addEventListener('click', () => {
    applyLanguage(currentLang === 'id' ? 'en' : 'id');
});

