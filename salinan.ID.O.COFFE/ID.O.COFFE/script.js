function addToCart(id) {
    const item = menuItems.find(i => i.id === id);
    if (!item) return;
    const existing = cart.find(i => i.id === id);
    if (existing) {
        existing.quantity += 1;
    } else {
        cart.push({ ...item, quantity: 1 });
    }
    // Bounce feedback on count badge
    cartCountEl.style.transform = 'scale(1.6)';
    setTimeout(() => cartCountEl.style.transform = 'scale(1)', 220);
    saveAndRenderCart();
}

function changeQuantity(id, delta) {
    const item = cart.find(i => i.id === id);
    if (!item) return;
    item.quantity += delta;
    if (item.quantity <= 0) cart = cart.filter(i => i.id !== id);
    saveAndRenderCart();
}

clearCartBtn.addEventListener('click', () => {
    const msg = currentLang === 'id' 
        ? 'Yakin mau mengosongkan keranjang?' 
        : 'Are you sure you want to clear your cart?';
    if (confirm(msg)) { cart = []; saveAndRenderCart(); }
});

function saveAndRenderCart() {
    localStorage.setItem('cart', JSON.stringify(cart));
    updateCartUI();
}

function updateCartUI() {
    const totalItems = cart.reduce((s, i) => s + i.quantity, 0);
    cartCountEl.textContent = totalItems;

    const lang = translations[currentLang];

    if (cart.length === 0) {
        cartItemsCont.innerHTML = `<p class="empty-cart-msg">${lang.cart_empty}</p>`;
        cartTotalEl.textContent = 'Rp 0';
        checkoutBtn.disabled = true;
        checkoutBtn.style.opacity = '0.5';
        return;
    }

    checkoutBtn.disabled = false;
    checkoutBtn.style.opacity = '1';
    cartItemsCont.innerHTML = '';

    let total = 0;
    cart.forEach(item => {
        const itemTotal = item.price * item.quantity;
        total += itemTotal;
        const el = document.createElement('div');
        el.className = 'cart-item';
        el.innerHTML = `
            <div class="cart-item-info">
                <img src="${item.img}" alt="${item.name}" class="cart-item-img">
                <div class="cart-item-details">
                    <h4>${item.name}</h4>
                    <span>Rp ${item.price.toLocaleString('id-ID')}</span>
                </div>
            </div>
            <div class="cart-item-controls">
                <button class="qty-btn" onclick="changeQuantity(${item.id}, -1)">-</button>
                <span>${item.quantity}</span>
                <button class="qty-btn" onclick="changeQuantity(${item.id}, 1)">+</button>
            </div>
        `;
        cartItemsCont.appendChild(el);
    });

    cartTotalEl.textContent = `Rp ${total.toLocaleString('id-ID')}`;
}

/* ==========================================================================
   CHECKOUT – In-Site Form
   ========================================================================== */
function openCheckoutModal() {
    if (cart.length === 0) return;

    const lang = translations[currentLang];

    // Build order summary
    let total = 0;
    let summaryHTML = `<div class="checkout-summary-title">${lang.summary_label}</div>`;
    cart.forEach(item => {
        const sub = item.price * item.quantity;
        total += sub;
        summaryHTML += `
            <div class="checkout-summary-item">
                <span>${item.name} ${item.icon} &times; ${item.quantity}</span>
                <strong>Rp ${sub.toLocaleString('id-ID')}</strong>
            </div>`;
    });
    checkoutSummary.innerHTML = summaryHTML;
    checkoutTotalEl.textContent = `Rp ${total.toLocaleString('id-ID')}`;

    // Reset form
    checkoutForm.reset();
    document.getElementById('err-name').textContent = '';
    document.getElementById('err-phone').textContent = '';
    document.getElementById('co-name').classList.remove('invalid');
    document.getElementById('co-phone').classList.remove('invalid');

    // Close cart, open checkout
    cartModal.classList.remove('active');
    checkoutModal.classList.add('active');
}

// Open checkout from cart
checkoutBtn.addEventListener('click', openCheckoutModal);

// Close checkout
closeCheckoutBtn.addEventListener('click', () => checkoutModal.classList.remove('active'));
checkoutModal.addEventListener('click', e => { if (e.target === checkoutModal) checkoutModal.classList.remove('active'); });

// Form submission
checkoutForm.addEventListener('submit', (e) => {
    e.preventDefault();
    const lang = translations[currentLang];

    const nameInput  = document.getElementById('co-name');
    const phoneInput = document.getElementById('co-phone');
    const errName    = document.getElementById('err-name');
    const errPhone   = document.getElementById('err-phone');

    let valid = true;

    // Validate name
    if (!nameInput.value.trim()) {
        errName.textContent = lang.err_name;
        nameInput.classList.add('invalid');
        valid = false;
    } else {
        errName.textContent = '';
        nameInput.classList.remove('invalid');
    }

    // Validate phone
    const phoneVal = phoneInput.value.trim();
    if (!phoneVal) {
        errPhone.textContent = lang.err_phone;
        phoneInput.classList.add('invalid');
        valid = false;
    } else if (!/^[0-9+\-() ]{8,15}$/.test(phoneVal)) {
        errPhone.textContent = lang.err_phone_invalid;
        phoneInput.classList.add('invalid');
        valid = false;
    } else {
        errPhone.textContent = '';
        phoneInput.classList.remove('invalid');
    }

    if (!valid) return;

    // Generate order ID
    const orderId = 'IDO-' + Date.now().toString().slice(-6);
    const orderType  = document.querySelector('input[name="order-type"]:checked').value;
    const payment    = document.querySelector('input[name="payment"]:checked').value;

    // Show success
    successOrderId.textContent = `# ${orderId}`;
    successMsg.innerHTML = translations[currentLang].success_msg;
    checkoutModal.classList.remove('active');
    successModal.classList.add('active');

    // Clear cart
    cart = [];
    saveAndRenderCart();
});

// Close success
closeSuccessBtn.addEventListener('click', () => {
    successModal.classList.remove('active');
});
successModal.addEventListener('click', e => { if (e.target === successModal) successModal.classList.remove('active'); });

